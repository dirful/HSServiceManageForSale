package com.cqut.entity.ProcessDescription;

import java.util.Date;
import com.cqut.service.tableCreator.ID;

public class ProcessDescription{
	
	@ID
	private String  ID;
	private String ProcessID;
	private String checkUserID;
	private int orderNumber;
	private int state;
	private String remark;
	private String submitAccountID;
	private int type;
	
	
	public String getID() {
		return ID;
	}	
	
	public void setID(String ID) {
		this.ID = ID;
	}
	public String getProcessID() {
		return ProcessID;
	}	
	
	public void setProcessID(String ProcessID) {
		this.ProcessID = ProcessID;
	}
	public String getCheckUserID() {
		return checkUserID;
	}	
	
	public void setCheckUserID(String checkUserID) {
		this.checkUserID = checkUserID;
	}
	public int getOrderNumber() {
		return orderNumber;
	}	
	
	public void setOrderNumber(int orderNumber) {
		this.orderNumber = orderNumber;
	}
	public int getState() {
		return state;
	}	
	
	public void setState(int state) {
		this.state = state;
	}
	public String getRemark() {
		return remark;
	}	
	
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getSubmitAccountID() {
		return submitAccountID;
	}	
	
	public void setSubmitAccountID(String submitAccountID) {
		this.submitAccountID = submitAccountID;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	@Override
	public String toString() {
		return "ProcessDescription [ID=" + ID + ", ProcessID=" + ProcessID
				+ ", checkUserID=" + checkUserID + ", orderNumber="
				+ orderNumber + ", state=" + state + ", remark=" + remark
				+ ", submitAccountID=" + submitAccountID + ", type=" + type
				+ "]";
	}
}
