package com.cqut.entity.module;

import com.cqut.service.tableCreator.ID;

public class Module {

	@ID
	private String MODULEID;
	private String MODULENAME;
	private String URL;
	private String PARENT;
	private String ICON;
	private int HASCHILD;
	private int ISENDOFMODULELEVEL;
	private int LEVEL0;
	private int ISFOLDER;
	private int SHOWMENUPAGE;
	private String CHILDSHOWTYPE;
	private String MODULETYPE;
	public String getMODULEID() {
		return MODULEID;
	}
	public void setMODULEID(String mODULEID) {
		MODULEID = mODULEID;
	}
	public String getMODULENAME() {
		return MODULENAME;
	}
	public void setMODULENAME(String mODULENAME) {
		MODULENAME = mODULENAME;
	}
	public String getURL() {
		return URL;
	}
	public void setURL(String uRL) {
		URL = uRL;
	}
	public String getPARENT() {
		return PARENT;
	}
	public void setPARENT(String pARENT) {
		PARENT = pARENT;
	}
	public String getICON() {
		return ICON;
	}
	public void setICON(String iCON) {
		ICON = iCON;
	}
	public int getISENDOFMODULELEVEL() {
		return ISENDOFMODULELEVEL;
	}
	public void setISENDOFMODULELEVEL(int iSENDOFMODULELEVEL) {
		ISENDOFMODULELEVEL = iSENDOFMODULELEVEL;
	}
	public int getLEVEL0() {
		return LEVEL0;
	}
	public void setLEVEL0(int lEVEL0) {
		LEVEL0 = lEVEL0;
	}
	public int getISFOLDER() {
		return ISFOLDER;
	}
	public void setISFOLDER(int iSFOLDER) {
		ISFOLDER = iSFOLDER;
	}
	public int getSHOWMENUPAGE() {
		return SHOWMENUPAGE;
	}
	public void setSHOWMENUPAGE(int sHOWMENUPAGE) {
		SHOWMENUPAGE = sHOWMENUPAGE;
	}
	public String getCHILDSHOWTYPE() {
		return CHILDSHOWTYPE;
	}
	public void setCHILDSHOWTYPE(String cHILDSHOWTYPE) {
		CHILDSHOWTYPE = cHILDSHOWTYPE;
	}
	public String getMODULETYPE() {
		return MODULETYPE;
	}
	public void setMODULETYPE(String mODULETYPE) {
		MODULETYPE = mODULETYPE;
	}
	public int getHASCHILD() {
		return HASCHILD;
	}
	public void setHASCHILD(int hASCHILD) {
		HASCHILD = hASCHILD;
	}
	@Override
	public String toString() {
		return "Module [MODULEID=" + MODULEID + ", MODULENAME=" + MODULENAME
				+ ", URL=" + URL + ", PARENT=" + PARENT + ", ICON=" + ICON
				+ ", HASCHILD=" + HASCHILD + ", ISENDOFMODULELEVEL="
				+ ISENDOFMODULELEVEL + ", LEVEL0=" + LEVEL0 + ", ISFOLDER="
				+ ISFOLDER + ", SHOWMENUPAGE=" + SHOWMENUPAGE
				+ ", CHILDSHOWTYPE=" + CHILDSHOWTYPE + ", MODULETYPE="
				+ MODULETYPE + "]";
	}

}
